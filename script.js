function login(){
  let log = document.querySelector('.container-signup')
  log.style.display = 'none'

  let sign = document.querySelector('.container-login')
  sign.style.display = 'block'
}


function signup(){
  let log = document.querySelector('.container-signup')
  log.style.display = 'block'

  let sign = document.querySelector('.container-login')
  sign.style.display = 'none'
}